    private void initCommon() throws SQLException {
        ConnectDB.getInstance();
        ConnectDB.connect();
        tinhLuongCongNhan_Dao = new TinhLuongCongNhan_Dao();
        tinhLuongHanhChanh_Dao = new TinhLuongHanhChanh_Dao();
        bieuDoTronPb = new BieuDoTron();
        bieuDoTronPx = new BieuDoTron();
        jPanel3.add(bieuDoTronPb.createDemoPanel("Phòng ban"));
        jPanel2.add(bieuDoTronPx.createDemoPanel("Phân xưởng"));
    }

    private void taoBieuDoPb(List<Object[]> luongHanhChanhs) {
        bieuDoTronPb.clearData();
        for (Object[] ob : luongHanhChanhs) {
            bieuDoTronPb.addData(String.valueOf(ob[1]), (float) ob[5]);
        }
    }

    private void taoBieuDoPx(List<Object[]> luongCongNhans) {
        bieuDoTronPx.clearData();
        for (Object[] ob : luongCongNhans) {
            bieuDoTronPx.addData(String.valueOf(ob[1]), (float) ob[4]);
        }
    }

    private void xuLyThayDoiNgayThang() {
        String thang = (String) cmbThang.getSelectedItem();
        int nam = ychNam.getValue();
        taoBieuDoPb(tinhLuongHanhChanh_Dao.layBaoCaoTongLuongPhongBanTheoThang(thang + "-" + nam));
        taoBieuDoPx(tinhLuongCongNhan_Dao.layBaoCaoTongLuongPhanXuongTheoThang(thang + "-" + nam));
    }
