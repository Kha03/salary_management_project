    private void doDuLieuCongDoanSanPham() {
        int i = 1;
        for(CongDoan cd : congDoan_Dao.getDanhSachCongDoan()){
            Object[] objects = {
                i++,
                cd.getTenCongDoan(),
                cd.getMaCongDoan(),
                "",
                "",
                cd.getGiaTien(),
                cd.getTienDo(),
            };
            modelChamCong.addRow(objects);
        }
    }
